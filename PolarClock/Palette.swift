import UIKit

extension UIColor {

    class func background() -> UIColor {
        return .kashmirBlue()
    }
}

extension UIColor {

    class func kashmirBlue() -> UIColor {
        return UIColor(r:79, g: 114, b: 142)
    }

    class func limeGreen() -> UIColor {
        return UIColor(r:109, g: 219, b: 188)
    }

    class func softCyan() -> UIColor {
        return UIColor(r:109, g: 195, b: 219)
    }

    class func softYellowGreen() -> UIColor {
        return UIColor(r:195, g: 219, b: 109)
    }

    class func softRed() -> UIColor {
        return UIColor(r:219, g: 133, b: 109)
    }

    class func softMagneta() -> UIColor {
        return UIColor(r:219, g: 109, b: 195)
    }
}