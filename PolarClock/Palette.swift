import UIKit

extension UIColor {

    class func background() -> UIColor {
        return .darkBlue()
    }
}

extension UIColor {

    class func darkBlue() -> UIColor {
        return UIColor(r:79, g: 114, b: 142)
    }

    class func yellow() -> UIColor {
        return UIColor(r:237, g: 247, b: 124)
    }

    class func orange() -> UIColor {
        return UIColor(r:254, g: 199, b: 135)
    }

    class func pink() -> UIColor {
        return UIColor(r:255, g: 123, b: 178)
    }

    class func green() -> UIColor {
        return UIColor(r:95, g: 255, b: 190)
    }

    class func blue() -> UIColor {
        return UIColor(r:119, g: 234, b: 252)
    }

    class func purple() -> UIColor {
        return UIColor(r:119, g: 168, b: 252)
    }
}